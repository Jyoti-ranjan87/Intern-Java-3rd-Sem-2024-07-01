
public class SubsequentNumbersInSeries {

	public static void main(String[] args) {
	
		  int currentNumber = 1;
	        System.out.println("First 10 subsequent numbers in the series:");
	        System.out.print(currentNumber + " ");
	        for (int i = 1; i <= 10; i++) {
	            currentNumber *= 2; 
	            System.out.print(currentNumber + " ");
		
	}

}
}